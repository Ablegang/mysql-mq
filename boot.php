define('ROOT',realpath('./'));

spl_autoload_register(function ($class) {
    $file_path = str_replace('\\', '/', $class);
    $file = $file_path . '.php';
    if (!file_exists($file)) {
        throw new Exception('找不到文件：' . $file);
    }

    include_once $file;
    return true;
});